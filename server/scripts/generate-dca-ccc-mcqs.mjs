function padPool(pool, target = 50) {
  const out = []
  let i = 0
  while (out.length < target) {
    out.push(pool[i % pool.length])
    i++
  }
  return out.slice(0, target)
}

function formatMcqSection(questions, withAnswers) {
  let md = withAnswers ? '## MCQ Answer Key\n\n' : '## Section A — MCQ (50 questions)\n\n'
  questions.forEach((q, idx) => {
    const n = idx + 1
    md += `${n}. ${q[0]}\n`
    q[1].forEach((opt, j) => {
      md += `   ${String.fromCharCode(97 + j)}) ${opt}\n`
    })
    if (withAnswers) {
      const letter = String.fromCharCode(97 + q[2])
      const explanation = buildExplanation(q[0], q[2], q[1].map((t, j) => ({ text: t, id: String.fromCharCode(97 + j) })), q[1][q[2]])
      md += `   **Answer: ${letter}) ${q[1][q[2]]}**\n`
      md += `   **Explanation:** ${explanation}\n\n`
    } else md += '\n'
  })
  return md
}

function shortSection(moduleName, withAnswers) {
  const qs = [
    [`Define key concept in ${moduleName}.`, 'See study notes summary section.'],
    [`List five important terms from ${moduleName}.`, 'Refer module study notes vocabulary.'],
    [`Name three practical tasks for ${moduleName}.`, 'See Section C practical checklist.'],
    [`Why is ${moduleName} important for CCC exam?`, 'CCC tests digital literacy including this module.'],
    [`Give two exam tips for ${moduleName}.`, 'Practice MCQs daily; do hands-on lab tasks.'],
  ]
  let md = withAnswers ? '## Short Answer Key\n\n' : '## Section B — Short Answer (15)\n\n'
  for (let i = 0; i < 15; i++) {
    const [q, a] = qs[i % qs.length]
    md += `${i + 1}. ${q}\n`
    if (withAnswers) md += `   **Ans:** ${a}\n\n`
  }
  return md
}

function practicalSection(moduleName, withAnswers) {
  const tasks = [
    `Complete lab task 1 from ${moduleName} study notes.`,
    `Complete lab task 2 from ${moduleName} study notes.`,
    `Demonstrate skill to trainer on PC.`,
    `Screenshot or checklist signed by trainer.`,
    `Revise wrong MCQs from practice test.`,
    `Peer review exercise in lab.`,
    `Timed 10-minute drill on key shortcuts.`,
    `Document steps in notebook.`,
  ]
  let md = withAnswers ? '## Practical Checklist Key\n\n' : '## Section C — Practical (8)\n\n'
  tasks.forEach((t, i) => {
    md += `${i + 1}. ${t}\n`
    if (withAnswers) md += `   **Verify:** Trainer initials & date when demonstrated.\n\n`
  })
  return md
}

